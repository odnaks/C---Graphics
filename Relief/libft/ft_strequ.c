int		ft_strequ(const char *s1, const char *s2)
{
	if (s1 && s2)
	{
		while (*s1 && *s2 && (*s1 == *s2))
		{
			s1++;
			s2++;
		}
		return (((unsigned char)*s1 - (unsigned char)*s2) == 0);
	}
	return (-1);
}
