#include <stdlib.h>
#include "libft.h"

char	*ft_itoa(int n)
{
	int		ln;
	int		t;
	char	*a;

	t = n;
	ln = 1 + (n < 0);
	while (t / 10)
	{
		ln++;
		t /= 10;
	}
	t = n;
	a = (char *)malloc(ln + 1);
	if (a)
	{
		a[ln] = '\0';
		while (ln--)
		{
			a[ln] = (t % 10 < 0 ? ('0' - (t % 10)) : ('0' + t % 10));
			t /= 10;
		}
		if (n < 0)
			a[0] = '-';
	}
	return (a);
}
